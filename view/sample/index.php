<p class="lead mb-5">
    Built to support the transition from legacy PHP programs to modern frameworks
</p>

<h2 class="fw-light">
    Make it simple.
</h2>
<p>
    Some legacy queries may be overly complicated and some legacy data
    may have unique structures that cannot or hardly can be converted into
    modern frameworks. This framework was made because of them!
</p>
<p>
    All you need to do is copy and paste your query and you are done!
</p>
<code class="bg-light rounded d-block p-3 mb-3">
    <span class="text-primary">DB</span>::<span class="text-dark">prepare(</span>-paste your query here-<span class="text-dark">)->execute()</span>;
</code>
<p>
    There is no such thing as 'model' here. Thus there is no need to 
    modify your table structures. <strong>BUT! Please DO modify them</strong>
    when you are about to use any modern framework if your structure
    is not suitable for the framework.
</p>